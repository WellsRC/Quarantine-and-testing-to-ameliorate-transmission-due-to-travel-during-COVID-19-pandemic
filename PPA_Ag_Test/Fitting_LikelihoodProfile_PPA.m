Dt=[ 1 2 3 4 5 6 7 ]; % Days since symptom onset
S=[7	8; 10	12; 1	2;5	5;3	4;2	3;1	4]';
opts= optimset('MaxIter',10^4,'MaxFunEvals',10^4,'TolFun',10^(-12),'TolX',10^(-12),'UseParallel',false,'Display','off');
[beta,MLE]=fmincon(@(z)Fit(z,Dt,S(1,:),S(2,:)),[1 -0.01],[],[],[],[],[0 -100],[100 0],[],opts);
MLE=-MLE;

save('BD Veritor_LR_Parameters.mat','beta','MLE','Dt','S');

